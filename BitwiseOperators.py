x = 10
y = 4

# Output: x & y is 0
print('x & y  is',x & y)

# Output: x | y is 14
print('x | y  is',x | y)

# Output:~x is -11
print('~x  is',~x)

# Output: x ^ y  is 14
print('x ^ y   is',x ^ y )

# Output: x >> 2 is 02
print('x >> 2  is',x >> 2)

# Output: x << y is 40
print('x << 2  is',x << 2)